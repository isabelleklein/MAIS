 		<h2>Historie der Gebäudereiniger-Innung Rheinhessen-Pfalz</h2>
		
		Die Glas- und Gebäudereiniger-Innung der Pfalz wurde aufgrund der ersten Verordnung über den vorläufigen Aufbau des deutschen Handwerks vom 15. Juni 1934 mit Wirkung vom 1. Oktober 1934 gegründet. Der erste Obermeister war Friedrich Gottlieb Hehl, Ludwigshafen. <br><br>
		Am 27.04.1954 wurde die Innung von „Glas- und Gebäudereiniger-Innung der Pfalz“ in „Gebäudereiniger-Innung der Pfalz“ umbenannt. <br><br>
		Am 7.12.2004 haben die Mitglieder der Gebäudereiniger-Innung der Pfalz sowie der Gebäudereiniger-Innung Rheinhessen beschlossen, sich ab 1.1.2005 zur Gebäudereiniger-Innung Rheinhessen-Pfalz zusammenzuschließen.<br><br>
 	
