		mit der anliegenden Ver&ouml;ffentlichung im Bundesgesetzblatt I Nr. 39 vom 29.07.2011 ist das Gesetz zur &Auml;nderung des Arbeitnehmer&uuml;berlassungs- gesetzes und des Schwarzarbeitsbek&auml;mpfungsgesetzes am 30.07.2011 in Kraft getreten.<br><br>
		Mit dem Gesetz werden den Beh&ouml;rden der Zollverwaltung die Befugnisse zur Kontrolle der Einhaltung der Lohnuntergrenze einger&auml;umt. Es &uuml;bertr&auml;gt das aus dem Bereich des Arbeitnehmer-Entsendegesetzes bekannte Kontroll- und Sanktionsinstrumentarium in das Arbeitnehmer&uuml;berlassungs- gesetz und schafft entsprechende Pflichten f&uuml;r Zeitarbeitsunternehmen und Einsatzbetriebe.<br><br>
		Zudem erhalten Sie anliegend auch eine aktualisierte Fassung des Arbeitnehmer&uuml;berlassungsgesetzes, aus der die letzten &Auml;nderungen, die dieses Gesetz betreffen, farblich gekennzeichnet sind.<br><br>
