		21.11.2011<br>
		<b>Neue Tarifvertr&auml;ge ab 2012
		Termin f&uuml;r Verhandlung der Allgemeinverbindlichkeit Anfang Dezember</b><br>
Sehr geehrte Damen und Herren,<br><br>
als Anlage &uuml;bersenden wir Ihnen die folgenden neuen Tarifvertr&auml;ge ab 2012 in Dateiform zu Ihrer Kenntnisnahme.
<li>Rahmentarifvertrag 2012</li>
<li>Lohntarifvertrag 2012/2013</li>
<li>Mindestlohntarifvertrag 2012/2013</li>
Nach Aussage des Bundesministeriums f&uuml;r Arbeit und Soziales (BMAS) soll voraussichtlich in der 49. KW Anfang Dezember der zust&auml;ndige Tarifausschuss &uuml;ber Allgemeinverbindlicherkl&auml;rung des Rahmentarifvertrages und des Mindestlohntarifvertrages beraten und entscheiden. Damit w&auml;re die Allgemeinverbindlichkeit zum 1.1.2012 durch rechtzeitige Ver&ouml;ffentlichung im Bundesanzeiger gewahrt.<br>
Erl&auml;uterung zur Lohngruppe 6/6a im Lohntarifvertrag und Lohngruppe 6 im Mindestlohntarifvertrag:<br>
Aufgrund der politischen Situation und den Mehrheitsverh&auml;ltnissen im Tarifausschuss des BMAS bestand keine M&ouml;glichkeit, die Tariferh&ouml;hung 2012/2013 der Lohngruppe 6 allgemeinverbindlich erkl&auml;ren zu lassen. Damit bestand nur die Alternative, die Lohngruppe 6 ganz freizugeben, oder zumindest eine Lohnuntergrenze in H&ouml;he des Mindestlohns 2011 "einzufrieren". Zur Verhinderung einer sicheren Ablehnung der Allgemeinverbindlicherkl&auml;rung der Lohngruppe 6 im Tarifausschuss des BMAS haben sich die Tarifparteien entschlossen, den Antrag in H&ouml;he des bisherigen Mindestlohns 2011 zu stellen.<br><br>

Damit betr&auml;gt - vorbehaltlich der Zustimmung des Tarifausschusses des BMAS - der Mindestlohn der Lohngruppe 6 im Westen Euro 11,33 (2012 und 2013). Im Osten soll der Mindestlohn Euro 8,88 (2012) und Euro 9,00 (2013) betragen. Die Erh&ouml;hung in 2013 auf Euro 9,00 ist m&ouml;glich, damit der Lohn der Lohngruppe 6 im Osten nicht unter dem Mindestlohn der Lohngruppe 1 im Westen liegt.<br><br>
Tarifrechtlich war es nicht zul&auml;ssig, den unterschiedlichen Lohnh&ouml;hen im Mindestlohntarifvertrag und im Lohntarifvertrag die gleiche Bezeichnung "Lohngruppe 6" zu geben und identisch zu definieren. Daher wird im Lohntarifvertrag unterschieden zwischen einer Lohngruppe 6 und einer Lohngruppe 6a.<br><br>
Die Lohngruppe 6 im Lohntarifvertrag ist identisch mit der Lohngruppe 6 des Mindestlohntarifvertrages.<br>
Die Lohngruppe 6a erh&auml;lt nach Paragr. 2 Satz 2 des Lohntarifvertrages die folgende Definition: "Besch&auml;ftigte der Lohngruppe 6 nach dreimonatiger T&auml;tigkeit im Geb&auml;udereiniger-Handwerk."<br><br>
Bei der Berechnung der "dreimonatigen T&auml;tigkeit" kommt es nicht auf die aktuelle Dauer Betriebszugeh&ouml;rigkeit an, sondern auf die T&auml;tigkeit im Sinne der Lohngruppe 6 in der Branche insgesamt (also incl. aller Vorbesch&auml;ftigungszeiten).<br><br>
<b>Den Tarifvertragsparteien ist bewusst, dass diese Regelungen der Lohngruppen 6 und 6a im Mindestlohn- und Lohntarifvertrag kompliziert und komplex in der betrieblichen Umsetzung sind. Aber leider war dies die einzige M&ouml;glichkeit, einen fortdauernden Mindestlohn f&uuml;r die Glas- und Fassadenreinigung zu erhalten.</b><br><br>&Uuml;ber die Allgemeinverbindlicherkl&auml;rung des Rahmentarifvertrages und des Mindestlohntarifvertrages werden wir die Mitgliedsbetriebe unmittelbar nach der Sitzung des Tarifausschusses informieren.<br>
