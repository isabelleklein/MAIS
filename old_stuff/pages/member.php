		<b>BIV-Rundschreiben: Lehrmaterial Kalkulation 2012</b><br><br>
		
		Sehr geehrte Damen und Herren,<br>
 
nachdem der Tarifausschuss beim Bundesarbeitsministerium die Allgemeinverbindlichkeit unseres Mindestlohntarifvertrages empfohlen hat, ver&ouml;ffentlichen wir hiermit das grundlegend &uuml;berarbeitete Lehrmaterial Kalkulation mit den aktuellen gesetzlichen und tariflichen Bestimmungen f&uuml;r das Jahr 2012.<br>
 
Neben der Einarbeitung der neuen Tarifvertr&auml;ge wurde insbesondere die Struktur hinsichtlich der Sozialversicherungsbeitr&auml;ge auf Soziall&ouml;hne &uuml;berarbeitet. Die Kalkulationszuschl&auml;ge f&uuml;r diese Beitr&auml;ge sind ab sofort bei der jeweiligen Soziallohnposition aufgef&uuml;hrt und nicht mehr wie zuvor als Gesamtbeitrag f&uuml;r Soziall&ouml;hne.<br>
 
